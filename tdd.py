def add(a: int, b: int) -> int:
    """Add a + b.

    This function sums up two arguments and return the result.

    Parameters
    ----------
    a : int
        A value to be added and return. Must support addition with b.
    b : int
        A value to be added and return. Must support addition with a.

    Examples
    --------
    >>> add(1, 2)
    3
    """
    return a + b
